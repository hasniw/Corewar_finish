int		ft_wprocess4(t_id *e, long double x)
{
	long	r[2];

	r[0] = 0;
	r[1] = 0;
	(void)x;
	if ((e->fm & 8) != 8 || ft_isnan(x) || \
	ft_isinf(x) || ((e->fm & 8) == 8 && (e->fm & 4) == 4))
		((r[1] = ft_iputxchar(' ', e->w)) >= 0) ? (r[0] += r[1]) : 0;
	return ((int)((r[1] < 0) ? -1 : r[0]));
}
